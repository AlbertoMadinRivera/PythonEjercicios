# Ejercicio 2: Exponer el uso básico de la función print.

# Ejemplo básico
print('Este es un ejemplo básico.')

# Concatenar sin espacio entre las cadenas
print('Este es un ejemplo básico.', end='')
print('Este es un ejemplo básico.', end='')

# Imprimir una línea en blanco
print()

# Imprimir palabras separadas por espacio y guión
print('Python', 'es', 'tremendo!')
print('Python', 'es', 'tremendo!', sep='-')

# Imprimir usando format
print('{} es {}'.format('Python', 'tremendo!'))

# Imprimir una lista
numeros = [2, 3, 5, 7, 11]
print(numeros)

# Imprimir un diccionario
capitales = {'Colombia': 'Bogotá', 'Perú': 'Lima'}
print(capitales)
